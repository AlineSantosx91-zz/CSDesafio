--SET DATABASE SQL SYNTAX ORA TRUE;
DROP TABLE Customer IF EXISTS;
DROP TABLE Phone IF EXISTS;


CREATE TABLE Customer(Id BIGINT GENERATED BY DEFAULT AS IDENTITY (INCREMENT BY 1) NOT NULL,
UUID VARCHAR(255), Created_Timestamp TIMESTAMP, Active TINYINT, Email VARCHAR(255), Password VARCHAR(255),
Last_Login TIMESTAMP, Modified TIMESTAMP, Token VARCHAR(255),Name VARCHAR(255), PRIMARY KEY (Id)
);

CREATE TABLE Phone (Id BIGINT GENERATED BY DEFAULT AS IDENTITY (INCREMENT BY 1) NOT NULL,
UUID VARCHAR(255), Created_Timestamp TIMESTAMP, DDD VARCHAR(3), Numero VARCHAR(20), Customer_Id BIGINT,
PRIMARY KEY (Id));