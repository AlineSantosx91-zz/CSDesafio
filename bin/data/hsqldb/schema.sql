--SET DATABASE SQL SYNTAX ORA TRUE;
DROP TABLE User IF EXISTS;
DROP TABLE Customer IF EXISTS;
DROP TABLE Phone IF EXISTS;
DROP TABLE Customer_Phones IF EXISTS;



CREATE MEMORY TABLE User (Id BIGINT GENERATED BY DEFAULT AS IDENTITY (INCREMENT BY 1) NOT NULL,
UUID VARCHAR(255), Created_Timestamp TIMESTAMP, Active TINYINT, Email VARCHAR(255), Password VARCHAR(255),
Last_Login TIMESTAMP, Modified TIMESTAMP, Token VARCHAR(255), PRIMARY KEY (Id));

CREATE MEMORY TABLE Customer (Id BIGINT GENERATED BY DEFAULT AS IDENTITY (INCREMENT BY 1) NOT NULL,
UUID VARCHAR(255), Created_Timestamp TIMESTAMP, Active TINYINT, Name VARCHAR(255), PRIMARY KEY (Id)
);

CREATE MEMORY TABLE Phone (Id BIGINT GENERATED BY DEFAULT AS IDENTITY (INCREMENT BY 1) NOT NULL,
UUID VARCHAR(255), Created_Timestamp TIMESTAMP, Active TINYINT, DDD VARCHAR(3), Number VARCHAR(20),
Customer_Id BIGINT, PRIMARY KEY (Id), FOREIGN KEY (Customer_Id) REFERENCES Customer(Id));

CREATE MEMORY TABLE Customer_Phones (Id BIGINT GENERATED BY DEFAULT AS IDENTITY (INCREMENT BY 1) NOT NULL,
Phones_Id BIGINT, Customer_Id BIGINT);


